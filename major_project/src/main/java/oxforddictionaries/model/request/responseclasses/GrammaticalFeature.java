package oxforddictionaries.model.request.responseclasses;

/**
 * POJO
 */
// Inline model 3
public class GrammaticalFeature {
    private String id;
    private String text;
    private String type;

    /**
     * @return id
     */
    public String getId() {
        return id;
    }

    /**
     * @return text
     */
    public String getText() {
        return text;
    }

    /**
     * @return type
     */
    public String getType() {
        return type;
    }
}
