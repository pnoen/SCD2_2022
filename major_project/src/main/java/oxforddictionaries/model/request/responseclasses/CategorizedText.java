package oxforddictionaries.model.request.responseclasses;

/**
 * POJO
 */
// Inline model 4
public class CategorizedText {
    private String id; // optional
    private String text;
    private String type;

    /**
     * @return id
     */
    public String getId() {
        return id;
    }

    /**
     * @return text
     */
    public String getText() {
        return text;
    }

    /**
     * @return type
     */
    public String getType() {
        return type;
    }
}
